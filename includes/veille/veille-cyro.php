                    <p class="mx-5 text-justify">Cyro est un robot créé par des élèves de l'Université de Southampton en Angleterre.
                    Ce robot est concrètement un calamar robotique, ce robot est constitué de 8 tentacules de métales mises en mouvement
                    par un ressort qui se compresse et de décompresse entrainant celles-ci. Basé sur des calculs sur l'oscillation des 
                    vagues et des courants marins, celui-ci peut se déplacer exactement comme un calamar le ferait. Une forme aérodynamique 
